# Создайте словарь со списком вещей для похода в качестве ключа и их массой в качестве значения. 
# Определите какие вещи влезут в рюкзак передав его максимальную грузоподъёмность.
# Достаточно вернуть один допустимый вариант.*Верните все возможные варианты комплектации рюкзака.

from itertools import combinations

# Создаем словарь с вещами и их массой
gear = {
    "палатка": 2.5,
    "спальный мешок": 1.0,
    "Турист. коврик": 0.5,
    "плита": 1.2,
    "еда": 3.0,
    "вода": 1.0,
    "фонарик": 0.3,
    "аптечка": 0.5,
    "одежда": 1.5,
    "инструменты": 1.0
}

# Максимальная грузоподъемность рюкзака
max_weight = 5.0

# Находим все возможные варианты комплектации рюкзака
valid_combinations = []
for r in range(1, len(gear) + 1):
    for combo in combinations(gear.items(), r):
        total_weight = sum(item[1] for item in combo)
        if total_weight <= max_weight:
            valid_combinations.append(combo)

# Выводим один допустимый вариант
if valid_combinations:
    print("Один допустимый вариант комплектации рюкзака:")
    print(valid_combinations[0])
else:
    print("Нет допустимых вариантов комплектации рюкзака.")

# Выводим все возможные варианты комплектации рюкзака
print("\nВсе возможные варианты комплектации рюкзака:")
for combo in valid_combinations:
    print(combo)